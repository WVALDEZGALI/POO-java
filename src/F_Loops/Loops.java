package F_Loops;

public class Loops {
    public static void main(String[] args) {

        // For

        for (int i = 1; i < 6; i++) {
            System.out.println("Hello JAVA: " + i);
        }

        String[] names = {"Camilo", "Ana", "Mario", "Olga", "Diego",};

        for (int i = 0; i < names.length; i++) {
            System.out.println(names[i]);
        }
    }
}
